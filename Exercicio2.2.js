// 1. Criando a variável anoNascimento como número
let anoNascimento = 2000;

// 2. Convertendo anoNascimento para String e exibindo o tipo de dado
let anoNascimentoString = String(anoNascimento);
console.log("Tipo de anoNascimentoString:", typeof anoNascimentoString); // Deve exibir "string"

// 3. Criando a variável peso como String e convertendo para número
let peso = "80.5";
let pesoNumero = Number(peso);
console.log("Tipo de pesoNumero:", typeof pesoNumero); // Deve exibir "number"
console.log("Valor de pesoNumero:", pesoNumero); // Deve exibir 80.5
